    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos, Cafeteria.Usuario, Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Moneda where ID_Moneda = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_id.setText(rs.getString("ID_Moneda"));
                txt_nombre.setText(rs.getString("Nombre_Moneda"));
                txt_tipo.setText(rs.getString("Tipo_Cambio"));
                txt_estatus.setText(rs.getString("Estatus_Moneda"));

            } else {
                JOptionPane.showMessageDialog(null, "Puesto no registrada.");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_btn_buscarActionPerformed

