    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        // TODO add your handling code here:
        try{
            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos,Cafeteria.Usuario,Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Proveedores where ID_Proveedor = ?");
            pst.setString(1, txt_Buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_ID_Proveedor.setText(rs.getString("ID_Proveedor"));
                txt_Nombre_Proveedor.setText(rs.getString("Nombre_Proveedor"));
                txt_Direccion_Proveedor.setText(rs.getString("Direccion_Proveedor"));
                txt_Telefono_Proveedor.setText(rs.getString("Telefono_Proveedor"));
            } else {
                JOptionPane.showMessageDialog(null, "Proveedor no registrado.");
            }

        }catch (Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton_BuscarActionPerformed


