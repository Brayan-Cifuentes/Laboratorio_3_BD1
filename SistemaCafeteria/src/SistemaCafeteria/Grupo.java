            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos, Cafeteria.Usuario, Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("insert into Grupo values(?,?)");

            pst.setString(1, txt_idgrupo.getText().trim());
