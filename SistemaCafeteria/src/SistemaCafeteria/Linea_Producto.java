    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos, Cafeteria.Usuario, Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Linea_Producto where ID_Linea = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_idlinea.setText(rs.getString("ID_Linea"));
                txt_nombre.setText(rs.getString("Nombre_Linea"));
                txt_estado.setText(rs.getString("Estado_Linea"));

            } else {
                JOptionPane.showMessageDialog(null, "Linea no registrada.");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //Codigo que permite insertar registros en al base de datos
        try {
            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos, Cafeteria.Usuario, Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("insert into Linea_Producto values(?,?,?)");

            pst.setString(1, txt_idlinea.getText().trim());
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_estado.getText().trim());

            pst.executeUpdate();

            txt_idlinea.setText("");
            txt_nombre.setText("");
            txt_estado.setText("");

            lbl_estatus.setText("Registro exitoso.");
        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite modificar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();

            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos, Cafeteria.Usuario, Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("update Linea_Producto set ID_Linea = ?, Nombre_Linea=?, Estado_Linea=? where ID_Linea = " + ID);

            pst.setString(1, txt_idlinea.getText().trim());
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_estado.getText().trim());
            pst.executeUpdate();

            txt_buscar.setText("");
            lbl_estatus.setText("Modificación exitosa.");

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

