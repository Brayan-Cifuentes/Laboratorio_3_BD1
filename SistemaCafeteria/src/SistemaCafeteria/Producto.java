    private void btn_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarActionPerformed
  try{
            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos,Cafeteria.Usuario,Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("select * from Productos where ID_Producto = ?");
            pst.setString(1, txt_Buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_Id.setText(rs.getString("ID_Producto"));
                txt_Nombre.setText(rs.getString("Nombre_Producto"));
                txt_IdMarca.setText(rs.getString("ID_Marca"));
                txt_IdLinea.setText(rs.getString("ID_Linea"));
                txt_Precio.setText(rs.getString("Precio_Producto"));
                txt_Costo.setText(rs.getString("Costo_Producto"));
            } else {
                JOptionPane.showMessageDialog(null, "Producto no registrado.");
            }

        }catch (Exception e)
        {
            System.out.println(e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_BuscarActionPerformed


