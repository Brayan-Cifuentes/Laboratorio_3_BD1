    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed

 try {
            String ID = txt_Buscar.getText().trim();

            Connection cn = DriverManager.getConnection(Cafeteria.Base_de_Datos,Cafeteria.Usuario,Cafeteria.Clave);
            PreparedStatement pst = cn.prepareStatement("update Productos set ID_Producto = ?,Marca = ?,ID_Linea = ?, Precio_Producto = ?,Costo_Producto= ? where ID_Producto = " + ID);

            pst.setString(1, txt_Id.getText().trim());
            pst.setString(2, txt_Nombre.getText().trim());
            pst.setString(3, txt_IdMarca.getText().trim());
            pst.setString(4, txt_IdLinea.getText().trim());
            pst.setString(5, txt_Precio.getText().trim());
            pst.setString(6, txt_Costo.getText().trim());
            
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "¡MODIFICACION EXITOSO!");

        } catch (Exception e)
        {
            System.out.println(e);
        }


        // TODO add your handling code here:
    }//GEN-LAST:event_btn_ModificarActionPerformed

